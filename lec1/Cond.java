public class Cond {
    public static void main(String[] args) {
        int n = 79;
        if(n>=50){
            System.out.println("Hey");
        }
        if(n>100){
            System.out.println("Bye");
        }
        if(n%2 == 0){
            System.out.println("Even");
        }else{
            System.out.println("Odd");
        }
    }
    
}
